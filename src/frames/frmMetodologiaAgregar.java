    private void lblAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblAgregarMouseClicked
        String id=txtCodigo.getText();
        String metodologia=txtMetodologia.getText();
        String descripcion=txtDescripcion.getText();
        
        
        
        if (id.isEmpty() && metodologia.isEmpty() && descripcion.isEmpty()) {
            JOptionPane.showMessageDialog(null, "rellene todos los campos por favor");
        }else{
            try{
                Metodologia meto=new Metodologia();
                meto.setIdMetodologia(id);
                meto.setMetodologia(metodologia);
                meto.setDescripcion(descripcion);
                ControladorMetodologia.Agregar(meto);
                JOptionPane.showMessageDialog(null,"Todo se ejecuto con exito");
                limpiar();
                } catch (ErrorPlanificacion ex) {
                    Logger.getLogger(frmMetodologiaAgregar.class.getName()).log(Level.SEVERE, null, ex);
                }
             
            }
    }//GEN-LAST:event_lblAgregarMouseClicked

