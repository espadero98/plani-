    private void lblAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblAgregarMouseClicked
        String id=txtCodigo.getText();
        String nombre=txtNombre.getText();
        String unidad=cmbSubUnidad.getSelectedItem().toString();
        String pdf=txtPdf.getText();
        
       
        
        
        
        if (id.isEmpty() && nombre.isEmpty() && pdf.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Rellene todos los campos por favor");
        }else{
            try{
                SubUnidad sub=new SubUnidad();
                sub.setIdSubUnidad(id);
                String idUnidad=ControladorSubUnidad.Obtener(unidad);
                sub.setNombre(nombre);
                sub.setNombrePdf(pdf);
                
                //Conviertiendo a bytes el pdf
                File ruta=new File(ruta_archivo);
                byte[] mipdf = new byte[(int) ruta.length()];
                InputStream input = new FileInputStream(ruta);
                input.read(mipdf);
                sub.setPdf(mipdf);
                
                sub.setIdUnidad(idUnidad);
                ControladorSubUnidad.Agregar(sub);
                JOptionPane.showMessageDialog(null,"Todo se ejecuto con exito");
                limpiar();
                } catch (ErrorPlanificacion ex) {
                    Logger.getLogger(frmSubUnidadAgregar.class.getName()).log(Level.SEVERE, null, ex);
                } catch (FileNotFoundException ex) {
                Logger.getLogger(frmSubUnidadAgregar.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(frmSubUnidadAgregar.class.getName()).log(Level.SEVERE, null, ex);
            }
             
            }
    }//GEN-LAST:event_lblAgregarMouseClicked

