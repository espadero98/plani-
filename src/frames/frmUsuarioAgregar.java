    private void lblAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblAgregarMouseClicked
        String id=txtCodigo.getText();
        String usuario=txtUsuario.getText();
        String password=txtPassword.getText();
        String nombre=txtNombre.getText();
        String apellidos=txtApellidos.getText();
        String edad=txtEdad.getText();
        
        
        
        if (id.isEmpty() && usuario.isEmpty() && password.isEmpty() && nombre.isEmpty() && apellidos.isEmpty() && edad.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Rellene todos los campos por favor");
        }else{
            try{
                Usuario usu=new Usuario();
                usu.setIdUsuario(id);
                usu.setUsuario(usuario);
                usu.setPassword(password);
                usu.setNombre(nombre);
                usu.setApellido(apellidos);
                usu.setEdad(Integer.parseInt(edad));
                ControladorUsuario.Agregar(usu);
                JOptionPane.showMessageDialog(null,"Todo se ejecuto con exito");
                limpiar();
                } catch (ErrorPlanificacion ex) {
                    Logger.getLogger(frmUsuarioAgregar.class.getName()).log(Level.SEVERE, null, ex);
                }
             
            }
    }//GEN-LAST:event_lblAgregarMouseClicked

